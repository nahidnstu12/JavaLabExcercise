    public void arithmatic_operation()
    {
        String answer;
        last = Double.parseDouble(jfield.getText());
        switch(calculation)
        {
            case 1:
                res = num + last;
                answer = String.format("%.2f", res);
                jfield.setText((answer));
                break;
            case 2:
                res = num - last;
                answer = String.format("%.2f", res);
                jfield.setText((answer));
                break; 
            case 3:
                res = num * last;
                answer = String.format("%.2f", res);
                jfield.setText((answer));
                break;
            case 4:
                res = num / last;
                answer = String.format("%.2f", res);
                jfield.setText((answer));
                break;    
            case 5:
                res = num % last;
                answer = String.format("%.2f", res);
                jfield.setText((answer));
                break;   
             case 6:
                res =  Double.parseDouble(jfield.getText());
                jfield.setText(Double.toString(Math.sqrt(res)));
                break;   
        }
        //jfield.setText("");
    }
