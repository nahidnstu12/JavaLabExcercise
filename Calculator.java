    private void EqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualActionPerformed
        // TODO add your handling code here:
        String answer;
        last = Double.parseDouble(jfield.getText());
        if(operation == "+")
        {
            res = num + last;
            answer = String.format("%.2f", res);
            jfield.setText((answer));
        }
       else if(operation == "-")
        {
            res = num - last;
            answer = String.format("%.2f", res);
            jfield.setText((answer));
        }
       else if(operation == "*")
        {
            res = num * last;
            answer = String.format("%.2f", res);
            jfield.setText((answer));
        }
       else if(operation == "/")
        {
            res = num / last;
            answer = String.format("%.2f", res);
            jfield.setText((answer));
        }
       else if(operation == "%")
        {
            res = num % last;
            answer = String.format("%.2f", res);
            jfield.setText((answer));
        }
       else if(operation == "sqrt")
       {
            res =  Double.parseDouble(jfield.getText());
             jLabel1.setText( " sqrt "+last);
            jfield.setText(Double.toString(Math.sqrt(res)));
       }
       else if(operation.equals("7"))
       {
           res =  Double.parseDouble(jfield.getText());
           res = 1/res;
             //answer = String.format("%.2f", res);
            jfield.setText(Double.toString(res));
       }
                
        //arithmatic_operation();
        jLabel1.setText("");
        
    }//GEN-LAST:event_EqualActionPerformed

